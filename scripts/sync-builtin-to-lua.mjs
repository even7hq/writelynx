import { writeFileSync, mkdirSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";
import validators from "../packages/builtin-rules/validation/validators.json" with { type: "json" };
import fieldRules from "../packages/builtin-rules/redaction/field.rules.json" with { type: "json" };
import patternRules from "../packages/builtin-rules/redaction/pattern.rules.json" with { type: "json" };

const root = join(dirname(fileURLToPath(import.meta.url)), "..");
const outPath = join(root, "packages/lua/src/writelynx/builtin_rules.lua");
const ruleset = {
    validators,
    field: { rules: fieldRules },
    pattern: { rules: patternRules }
};

const lua = `-- Generated by scripts/sync-builtin-to-lua.mjs - do not edit by hand
return ${toLua(ruleset)}
`;

mkdirSync(dirname(outPath), { recursive: true });
writeFileSync(outPath, lua);
console.log(`Wrote ${outPath}`);

/**
 * Converts a JSON-serializable value to a Lua literal string.
 *
 * @param {unknown} value
 * @returns {string}
 */
function toLua(value) {
    if (value === null) {
        return "nil";
    }

    if (typeof value === "boolean") {
        return value ? "true" : "false";
    }

    if (typeof value === "number") {
        return String(value);
    }

    if (typeof value === "string") {
        return JSON.stringify(value);
    }

    if (Array.isArray(value)) {
        const items = value.map((item) => toLua(item));
        return `{ ${items.join(", ")} }`;
    }

    const entries = Object.entries(value).map(([k, v]) => {
        const key = /^[a-zA-Z_][a-zA-Z0-9_]*$/.test(k) ? k : `[${JSON.stringify(k)}]`;
        return `${key} = ${toLua(v)}`;
    });

    return `{ ${entries.join(", ")} }`;
}
